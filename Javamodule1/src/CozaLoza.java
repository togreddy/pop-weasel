
public class CozaLoza {
	
	
	
	public static void main(String args[]){
		
		
		for(int i=1;i<=110;i++)
		{
			
			if(i%3==0)
			{
				System.out.print("coza ");
			}
			else if(i%5==0)
			{
				System.out.print("Loza ");
			}
			else if(i%7==0)
			{
				System.out.print("Woza ");
			}
			else if(i%11==0)
			{
				System.out.println(i);
			}
			else
			{
				System.out.print(i+ " ");
			}
			
			
			
			
			
		}
		
	}
	
	

}
